package codeup;

public class Q1108 {
	// 1108 : 반복 출력하기 2
	public static void main(String[] args) {
		final int H_COUNT = 20;
		final int W_COUNT = 30;

		for (int i = 0; i < H_COUNT; i++) {
			System.out.print("hello");
		}
		System.out.print(" ");
		for (int i = 0; i < W_COUNT; i++) {
			System.out.print("world");
		}
	}
}
